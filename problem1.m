function out = problem1()

c = 299792.458;
x = 0;
y = 0;
z = 6370;
d = 0;
r = zeros(4, 1);
m = zeros(4, 4);

for i = 1:5
    S1 = sqrt((x-15600)^(2)+(y-7540)^(2)+(z-20140)^(2));
    S2 = sqrt((x-18760)^(2)+(y-2750)^(2)+(z-18610)^(2));
    S3 = sqrt((x-17610)^(2)+(y-14630)^(2)+(z-13480)^(2));
    S4 = sqrt((x-19170)^(2)+(y-610)^(2)+(z-18390)^(2));

    m = jacobian(S1, S2, S3, S4, x, y, z);

    s1 = S1 - c*(0.07074 - d);
    s2 = S2 - c*(0.07220 - d);
    s3 = S3 - c*(0.07690 - d);
    s4 = S4 - c*(0.07242 - d);

    r(1,1) = s1;
    r(2,1) = s2;
    r(3,1) = s3;
    r(4,1) = s4;

    s = m\r;

    x = x - s(1);
    y = y - s(2);
    z = z - s(3);
    d = d - s(4);
end

out = [x, y, z, d];
end